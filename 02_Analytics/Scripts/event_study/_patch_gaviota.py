# ─── Efecto Gaviota agregado ──────────────────────────────────────────────────
C_GAVIOTA = code(
"# ── Gráfico Efecto Gaviota — promedio alineado en t=0 ───────────────────\n"
"def plot_seagull_effect(eventos, activos, pre_h=PRE_HOURS, post_h=POST_HOURS,\n"
"                         rolling=ROLLING_WIND):\n"
"    \"\"\"Alinea todas las ventanas en t=0 y grafica TPH normalizado promedio.\"\"\"\n"
"    N_BINS = int((pre_h + post_h) * 60 / 5)  # bins de 5 min\n"
"    t_eje  = np.linspace(-pre_h, post_h, N_BINS)\n\n"
"    fig, axes = plt.subplots(2, 2, figsize=(16, 10))\n"
"    fig.suptitle(\n"
"        'Efecto Gaviota — TPH Normalizado por Ventana T8\\n'\n"
"        '(1.0 = baseline pre-ventana | zona roja = ventana activa)',\n"
"        fontsize=12\n"
"    )\n"
"    COLORES_FASE = {'pre':'steelblue','durante':'tomato','post':'seagreen'}\n\n"
"    gaviota_data = {}\n"
"    for ax, a in zip(axes.flat, activos):\n"
"        col, op = f'{a}_tph', f'{a}_operando'\n"
"        series_norm = []\n"
"        for ev in eventos:\n"
"            df_w = build_event_window(ev, pre_h, post_h)\n"
"            if df_w.empty: continue\n"
"            pre_vals = df_w.loc[(df_w['fase']=='pre')&df_w[op], col]\n"
"            if len(pre_vals) < MIN_PRE_PTS: continue\n"
"            base = pre_vals.mean()\n"
"            if base < TPH_OPERATIONAL: continue\n"
"            # Resamplear a 5-min uniformes por t_rel_h\n"
"            df_w2 = df_w[['t_rel_h', col]].copy().set_index('t_rel_h')\n"
"            df_interp = df_w2.reindex(\n"
"                df_w2.index.union(t_eje)\n"
"            ).interpolate(method='index')[col].reindex(t_eje)\n"
"            series_norm.append(df_interp.values / base)\n\n"
"        if not series_norm:\n"
"            ax.axis('off'); continue\n"
"        mat = np.array(series_norm)  # (n_eventos, N_BINS)\n"
"        med = np.nanmedian(mat, axis=0)\n"
"        p25 = np.nanpercentile(mat, 25, axis=0)\n"
"        p75 = np.nanpercentile(mat, 75, axis=0)\n"
"        gaviota_data[a] = {'t':t_eje,'med':med,'p25':p25,'p75':p75,'n':len(series_norm)}\n\n"
"        # Plot\n"
"        ax.fill_between(t_eje, p25, p75, alpha=0.25, color=COLORES[a], label='IQR 25-75')\n"
"        ax.plot(t_eje, med, color=COLORES[a], linewidth=2.5, label=f'Mediana (n={len(series_norm)})')\n"
"        ax.axvspan(0, 0, alpha=0)  # dummy\n"
"        # Zona ventana (0 a fin promedio)\n"
"        dur_media = np.mean([(ev['fin']-ev['ini']).total_seconds()/3600 for ev in eventos])\n"
"        ax.axvspan(0, min(dur_media, post_h), alpha=0.15, color='red', label='Zona T8')\n"
"        ax.axvline(0,          color='red',   linewidth=1.5, linestyle='--', label='Inicio T8')\n"
"        ax.axvline(dur_media,  color='orange',linewidth=1.2, linestyle=':', label=f'Fin T8 prom ({dur_media:.0f}h)')\n"
"        ax.axhline(1.0, color='gray', linewidth=0.8, linestyle='-', alpha=0.6)\n"
"        ax.axhline(0.9, color='gray', linewidth=0.6, linestyle=':', alpha=0.5, label='90% baseline')\n"
"        ax.set_title(f'{a}', fontsize=10)\n"
"        ax.set_xlabel('Horas relativas al inicio de T8', fontsize=9)\n"
"        ax.set_ylabel('TPH / TPH baseline', fontsize=9)\n"
"        ax.set_xlim(-pre_h, post_h)\n"
"        ax.set_ylim(bottom=0)\n"
"        ax.legend(fontsize=7, loc='lower right')\n"
"        ax.grid(True, alpha=0.25)\n"
"        # Anotar mínimo\n"
"        idx_min = np.nanargmin(med)\n"
"        ax.annotate(f'Min: {med[idx_min]:.2f}\\n({t_eje[idx_min]:.0f}h)',\n"
"                    xy=(t_eje[idx_min], med[idx_min]),\n"
"                    xytext=(t_eje[idx_min]+3, med[idx_min]-0.05),\n"
"                    fontsize=8, color='red',\n"
"                    arrowprops=dict(arrowstyle='->', color='red', lw=1))\n"
"    plt.tight_layout()\n"
"    plt.savefig(OUT_GAV/'00_Efecto_Gaviota_Agregado.png', bbox_inches='tight', dpi=120)\n"
"    plt.show()\n"
"    return gaviota_data\n\n"
"print('Generando gráfico Efecto Gaviota agregado...')\n"
"gaviota_data = plot_seagull_effect(eventos_t8, ACTIVOS)\n"
"print('Guardado: outputs/figures/efecto_gaviota/00_Efecto_Gaviota_Agregado.png')"
)

