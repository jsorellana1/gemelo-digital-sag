# ─── Funciones reutilizables ─────────────────────────────────────────────────
C_FUNCS = code(
"# ── Funciones reutilizables ──────────────────────────────────────────────\n\n"
"def load_master_dataset():\n"
"    \"\"\"Retorna copia del dataset maestro ya cargado.\"\"\"\n"
"    return df_master.copy()\n\n"
"def get_t8_events(dm=None, min_horas=0):\n"
"    \"\"\"Detecta ventanas T8 continuas. Retorna lista de dicts {ini, fin, horas_total}.\"\"\"\n"
"    dm = dm if dm is not None else df_master\n"
"    dias = dm[dm['horas_t8'] > min_horas].sort_values('fecha')\n"
"    if dias.empty: return []\n"
"    evs = []; s = e = dias.iloc[0]['fecha']\n"
"    for _, row in dias.iloc[1:].iterrows():\n"
"        if (row['fecha'] - e).days <= 2:\n"
"            e = row['fecha']\n"
"        else:\n"
"            mask = (dm['fecha']>=s)&(dm['fecha']<=e)\n"
"            evs.append({'ini':s,'fin':e+pd.Timedelta(days=1),\n"
"                        'horas_total':dm[mask]['horas_t8'].sum(),\n"
"                        'n_dias':int(mask.sum())})\n"
"            s = e = row['fecha']\n"
"    mask = (dm['fecha']>=s)&(dm['fecha']<=e)\n"
"    evs.append({'ini':s,'fin':e+pd.Timedelta(days=1),\n"
"                'horas_total':dm[mask]['horas_t8'].sum(),'n_dias':int(mask.sum())})\n"
"    return evs\n\n"
"def build_event_window(ev, pre_h=PRE_HOURS, post_h=POST_HOURS):\n"
"    \"\"\"Extrae ventana temporal pre/durante/post de df_rend (5-min).\"\"\"\n"
"    ini, fin = ev['ini'], ev['fin']\n"
"    t0 = ini - pd.Timedelta(hours=pre_h)\n"
"    t1 = fin + pd.Timedelta(hours=post_h)\n"
"    mask = (df_rend['fecha'] >= t0) & (df_rend['fecha'] <= t1)\n"
"    df_w = df_rend[mask].copy()\n"
"    df_w['fase'] = 'pre'\n"
"    df_w.loc[(df_w['fecha']>=ini)&(df_w['fecha']<fin),'fase'] = 'durante'\n"
"    df_w.loc[df_w['fecha']>=fin,'fase'] = 'post'\n"
"    df_w['t_rel_h'] = (df_w['fecha'] - ini).dt.total_seconds() / 3600\n"
"    return df_w\n\n"
"def calculate_pre_post_metrics(df_w, activo, ini, fin):\n"
"    \"\"\"Calcula métricas pre/post para un activo en una ventana de evento.\"\"\"\n"
"    col, op = f'{activo}_tph', f'{activo}_operando'\n"
"    pre  = df_w.loc[(df_w['fase']=='pre')  & df_w[op], col]\n"
"    post = df_w.loc[(df_w['fase']=='post') & df_w[op], col]\n"
"    dur  = df_w.loc[(df_w['fase']=='durante') & df_w[op], col]\n"
"    if len(pre) < MIN_PRE_PTS: return None\n"
"    tph_pre  = pre.mean(); tph_post = post.mean() if len(post)>0 else np.nan\n"
"    tph_dur  = dur.mean() if len(dur)>0 else np.nan\n"
"    delta    = tph_post - tph_pre\n"
"    delta_pct= delta / tph_pre * 100 if tph_pre > 0 else np.nan\n"
"    # TPH mínimo post y su hora\n"
"    post_s = df_w.loc[df_w['fase']=='post', col]\n"
"    roll_post = post_s.rolling(12, min_periods=4).mean()\n"
"    tph_min = roll_post.min(); hora_min = np.nan\n"
"    if not pd.isna(tph_min) and not roll_post.empty:\n"
"        idx_min = roll_post.idxmin()\n"
"        hora_min = df_w.loc[idx_min,'t_rel_h'] if idx_min in df_w.index else np.nan\n"
"    # Tiempo de recuperación\n"
"    def t_rec(pct):\n"
"        thr = tph_pre * pct\n"
"        post_op = df_w.loc[(df_w['fase']=='post')&df_w[op], col].rolling(12,min_periods=4).mean()\n"
"        ok = post_op[post_op >= thr]\n"
"        if ok.empty: return np.nan\n"
"        return df_w.loc[ok.index[0],'t_rel_h']\n"
"    # Toneladas\n"
"    ton_pre  = df_w.loc[df_w['fase']=='pre',  f'{activo}_ton'].sum()\n"
"    ton_post = df_w.loc[df_w['fase']=='post', f'{activo}_ton'].sum()\n"
"    return {\n"
"        'activo':activo,'ini':ini.date(),'fin':fin.date(),\n"
"        'tph_pre':round(tph_pre,1),'tph_durante':round(tph_dur,1) if not np.isnan(tph_dur) else np.nan,\n"
"        'tph_post':round(tph_post,1) if not np.isnan(tph_post) else np.nan,\n"
"        'delta_tph':round(delta,1) if not np.isnan(delta) else np.nan,\n"
"        'delta_pct':round(delta_pct,2) if not np.isnan(delta_pct) else np.nan,\n"
"        'tph_min_post':round(tph_min,1),'hora_tph_min_post':round(hora_min,1),\n"
"        'h_rec_90':round(t_rec(0.90),1),'h_rec_95':round(t_rec(0.95),1),\n"
"        'ton_pre':round(ton_pre,0),'ton_post':round(ton_post,0),\n"
"        'delta_ton':round(ton_post-ton_pre,0),\n"
"    }\n\n"
"def classify_impact(delta_pct):\n"
"    \"\"\"Clasifica impacto según caída porcentual.\"\"\"\n"
"    if pd.isna(delta_pct) or delta_pct >= -5:  return 'Sin impacto'\n"
"    if delta_pct >= -10: return 'Leve'\n"
"    if delta_pct >= -20: return 'Medio'\n"
"    return 'Alto'\n\n"
"print('Funciones cargadas: load_master_dataset | get_t8_events | build_event_window')\n"
"print('                    calculate_pre_post_metrics | classify_impact')"
)

